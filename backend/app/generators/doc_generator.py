def _generate_learning_path(
    repo_path: Path, tech_data: dict, tree_data: dict, file_analyses: dict
) -> list[dict]:
    """Generate a step-by-step learning path for understanding the codebase."""
    steps = []

    # Step 1: README
    for name in ["README.md", "README.txt", "readme.md"]:
        if Path(_long_path(repo_path / name)).exists():
            steps.append({
                "step": 1,
                "title": "Read the README",
                "description": "Start with the project overview, installation, and usage instructions.",
                "file": name,
                "icon": "📖",
            })
            break
    if not steps:
        steps.append({
            "step": 1,
            "title": "Read the README",
            "description": "No README found — look for documentation in the /docs folder.",
            "file": None,
            "icon": "📖",
        })

    # Step 2: Configuration
    config_files = []
    for path in file_analyses:
        analysis = file_analyses[path]
        if analysis and "configuration" in (analysis.get("purpose") or "").lower():
            config_files.append(path)
    steps.append({
        "step": 2,
        "title": "Understand the Configuration",
        "description": "Review environment variables and configuration files.",
        "file": config_files[0] if config_files else ".env.example",
        "icon": "⚙️",
    })

    # Step 3: Entry point
    entries = [
        p for p, a in file_analyses.items()
        if a and "entry point" in (a.get("purpose") or "").lower()
    ]
    steps.append({
        "step": 3,
        "title": "Find the Entry Point",
        "description": "This is where the application starts. Trace execution from here.",
        "file": entries[0] if entries else None,
        "icon": "🚀",
    })

    # Step 4: Models/Schemas
    models = [p for p in file_analyses if "model" in p.lower() or "schema" in p.lower()]
    steps.append({
        "step": 4,
        "title": "Study Data Models",
        "description": "Understand the data structures the application works with.",
        "file": models[0] if models else None,
        "icon": "🗄️",
    })

    # Step 5: API/Routes
    apis = [p for p in file_analyses if "api" in p.lower() or "route" in p.lower()]
    steps.append({
        "step": 5,
        "title": "Explore APIs & Routes",
        "description": "Learn what endpoints or routes the application exposes.",
        "file": apis[0] if apis else None,
        "icon": "🔌",
    })

    # Step 6: Frontend/Views
    frontend = [p for p in file_analyses if p.endswith((".jsx", ".tsx", ".vue", ".html"))]
    if frontend:
        steps.append({
            "step": 6,
            "title": "Study the Frontend",
            "description": "Understand the user interface components and pages.",
            "file": frontend[0],
            "icon": "🎨",
        })

    # Step 7: Tests
    tests = [p for p in file_analyses if "test" in p.lower() or "spec" in p.lower()]
    steps.append({
        "step": len(steps) + 1,
        "title": "Run the Tests",
        "description": "Execute the test suite to verify everything works correctly.",
        "file": tests[0] if tests else None,
        "icon": "🧪",
    })

    return steps


def _generate_folder_guide(tree_data: dict) -> list[dict]:
    """Generate a folder-by-folder guide from the tree data."""
    folders = []

    def _collect_folders(node: dict, depth: int = 0):
        if node.get("type") == "directory" and depth <= 2:
            purpose = node.get("purpose", "")
            if node.get("name") and node["name"] not in {".", ""}:
                files = [c for c in node.get("children", []) if c.get("type") == "file"]
                subdirs = [c for c in node.get("children", []) if c.get("type") == "directory"]
                folders.append({
                    "name": node["name"],
                    "path": node["path"],
                    "purpose": purpose or f"Contains {node['name']}-related files",
                    "file_count": len(files),
                    "subdir_count": len(subdirs),
                    "depth": depth,
                })
            for child in node.get("children", []):
                _collect_folders(child, depth + 1)

    _collect_folders(tree_data.get("root", {}))
    return folders
